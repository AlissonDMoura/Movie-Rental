    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
          String fullName = nameTxtBox.getText().trim();
          String email = eMailTxtBox.getText().trim();
          String pass = passwordTxtBox.getText().trim();
          String pass2 = cPasswTxtBox.getText().trim();
          String cCard = cCardTxtBox.getText().trim();
          String expDate = ExpDateTxtBox.getText().trim();
          String ccv = cvvTxtBox.getText().trim();  
          boolean c1  = false;
          boolean c2  = false;
          boolean c3  = false;
          boolean c4 = false;
          boolean c5  = false;
          boolean c6 = false;
          
          
          if(fullName.isEmpty()){
          JOptionPane.showMessageDialog(this, "Your Full Name field is empty, Please insert your name");    
          }else{
            c1 = true;    
          }
          
          if(email.isEmpty()){
          JOptionPane.showMessageDialog(this, "Your Email field is empty, Please insert your email");     
          }else{
            c2 = true;    
          }
          
          if(pass.isEmpty() || pass2.isEmpty()){
            JOptionPane.showMessageDialog(this, "The field Password is empty, Please insert your password");       
            }
          
          if(pass.equals(pass2)){
          c3 = true;
          }else{
          JOptionPane.showMessageDialog(this, "Please make sure your passwords match, insert again");
          }
          
          if(cCard.length() != 16){
          JOptionPane.showMessageDialog(this, "Your Credit Card number is wrong, Please insert 16 digits");               
           }else{
            c4 = true;    
          }
          
          if(expDate.length() != 5){
           JOptionPane.showMessageDialog(this, "Your Expiry Date is wrong, Please insert 5 digits");               
          }else{
            c5 = true;    
          }
          
          if(ccv.length() != 3){
           
           JOptionPane.showMessageDialog(this, "Your CVV is wrong, Please insert 3 NUMBERS");               
           }else{
            c6 = true;    
          }
          
           if(c1 == true && c2 == true && c3 == true && c4 == true && c5 == true && c6 == true){
               
               int iccv = Integer.parseInt(ccv);
               
              try {
                  if(cart.NewUser(cCard, email, fullName, pass, iccv , expDate)){
                      int input = JOptionPane.showOptionDialog(null, "Payment Successful! \n\n Please wait for your DVD to be issued. \n\n  Thank You for using Xtra-Vision", "Confirmation", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                      if(input == JOptionPane.OK_OPTION){
                          this.dispose();    }                    }
                  else{
                      JOptionPane.showMessageDialog(this, "Account already registered for this Credit Card, please try a new one or login.");}
              } catch (SQLException ex) {
                  Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
              }
           }            
            
            
        
        
    
    }//GEN-LAST:event_jButton2ActionPerformed

